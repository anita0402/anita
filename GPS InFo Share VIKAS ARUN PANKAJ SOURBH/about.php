	  <h2> About Us </h2>
	    <div class="about">
		<p>Govt. Polytechnic,  Sundernagar, Distt. Mandi, is the first  premier institute  established in the year 1959 in Himachal Pradesh. The campus is situated on Chandigarh –Manali National  Highway (NH-21) at a distance of  175 Kms. fromChandigarhtowards Manali.. The campus is spread over 23.75 acres of lush  green land and surrounded by beautiful hills. The environment is ideally suited  for teaching –learning.</p>.

<p>Approved annual intake in all the courses  is 250 and affiliated to HP takniki Shiksha Board, Dharmshala. All the courses are duly approved by All India Council for Technical Education (AICTE), New Delhi. In addition to above 10% seats over and above sanctioned intake are being filled up under the Persons With Disabilities (PWD) Scheme of M.H.R.D.  Govt. of India, 5% seats over and above sanctioned intake under tution fees waiver scheme (TFW) and   10% seats over and above sanctioned intake are also being filled up under Lateral Entry System.</p>

<p>The institution has 10,023 Sq mt. built up instructional and administrative areas. There is a separate facility of hostel for both boys and Girls in the campus. About 80% of faculty and staff have been provided Govt residential facilities.</p>

<p>The admissions to the polytechnic are made on merit on the basis of marks obtained in Polytechnic Admission Test(PAT) conducted  by HP Takniki Shiksha Board Dharmshala. Five seats in all disciplines  are also kept reserved for Physically Disabled Candidates under the scheme of Integrating Person with Disability by the MHRD , Govt. of  India. Seats are also reserved for SC/ST/OBC/Sportmen/Children of Ex-Servicemen and Freedom Fighters and the candidates from backward areas as per the State Government policy.

   </p>
